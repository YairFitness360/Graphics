public class Point2D {
    private int x;
    private int y;

    /**
     * Constructor.
     * @param a is the x coordinate of the point.
     * @param b is the y coordinate of the point.
     */
    public Point2D(int a, int b) {
        x = a;
        y = b;
    }

    public int getX() {
        return this.x;
    }
    public int getY() {
        return this.y;
    }
}
